'''4 - Idade canina'''
idade_humana = int(input("Coloque a idade humana desejada: "))

cao_pequeno = idade_humana / 5

cao_medio = idade_humana / 6

cao_grande = idade_humana / 7

print("Caso você tenha um cão pequeno essa será sua idade = ", cao_pequeno, "\n"
      "Caso você tenha um cão médio essa será sua idade = ", cao_medio, "\n"
      "Caso você tenha um cão grande essa será sua idade = ", cao_grande)
